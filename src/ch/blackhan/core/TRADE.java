import org.zeromq.ZMQ.*;

///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

public class TRADE extends CLIENT {

    ///////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////

    public TRADE()
    {
        Socket requestor = this.mqm.setRequestorUri("tcp://fx-trade.blackhan.ch:6666");
        assert(requestor != null);

        Socket subscriber = this.mqm.setSubscriberUri("tcp://fx-trade.blackhan.ch:6667");
        assert(subscriber != null);
    }

    ///////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////
}
